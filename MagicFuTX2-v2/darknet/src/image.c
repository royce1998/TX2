// 判断人与目标是否远离
bool runaway(box person, box obj, int imagew, int imageh)
{
    float ratio = 1.6;
    int x1 = (person.x-person.w/2.)*imagew;
    int w1 = person.w*imagew;
    int x2 = (obj.x-obj.w/2.)*imagew;
    int w2 = obj.w*imagew;

    int dist = (x1-x2<0? x2-x1: x1-x2);
    int T = (w1/2.+w2/2.)*ratio;
    return (dist > T);

}

